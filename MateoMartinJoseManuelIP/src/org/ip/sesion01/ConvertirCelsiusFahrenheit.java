package org.ip.sesion01;

public class ConvertirCelsiusFahrenheit {
	public static void main(String[] args) {
		double temperatura, fahrenheit;
		temperatura = 29.00;
		fahrenheit = (9 * temperatura / 5) + 32;
		System.out.println("Los grados Fahrenheit correspondientes a 29 grados Celsius son => "
						+ fahrenheit);

	}

}
